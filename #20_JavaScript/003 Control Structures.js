//Control Structures
//Control structures are used to control the flow of execution in a program. JavaScript supports several types of control structures, including:
//
//if statements: execute a block of code if a certain condition is true
//for loops: execute a block of code a specified number of times
//while loops: execute a block of code as long as a certain condition is true
//switch statements: execute different blocks of code depending on the value of an expression//
//
// if statement
function printMultiplicationTable() {
  for (let i = 1; i <= 10; i++) {
    // for loop
    let row = "";
    for (let j = 1; j <= 10; j++) {
      row += `${i * j}\t`;
    }
    console.log(row);
  }

  let i = 1;
  // while loop
  while (i <= 10) {
    let j = 1;
    let row = "";
    while (j <= 10) {
      row += `${i * j}\t`;
      j++;
    }
    console.log(row);
    i++;
  }

  let day = 2;
  // switch statement
  switch (day) {
    case 1:
      console.log("Monday");
      break;
    case 2:
      console.log("Tuesday");
      break;
    case 3:
      console.log("Wednesday");
      break;
    case 4:
      console.log("Thursday");
      break;
    case 5:
      console.log("Friday");
      break;
    case 6:
      console.log("Saturday");
      break;
    case 7:
      console.log("Sunday");
      break;
    default:
      console.log("Invalid day");
      break;
  }
}

printMultiplicationTable();
//
//This code generates a multiplication table using a for loop and a nested for loop. 
//It also demonstrates a while loop that does the same thing, and a switch statement 
//that outputs the name of a day of the week based on a number. 
//The if statement is not used in this example, 
//but it can be added to perform conditional logic within the loops.//
////